// เลือกใช้งานฐานข้อมูล sample_mflix
use("sample_mflix");

// ใช้ Aggregation Pipeline เพื่อค้นหาภาพยนตร์ตามเงื่อนไข
db.movies.aggregate([

  // Stage 1 : $match
  // กรองข้อมูลภาพยนตร์ตามเงื่อนไขที่กำหนด
  {
    $match: {
      $and: [

        // เงื่อนไขที่ 1
        // เลือกภาพยนตร์ที่ออกฉายหลังปี 2010
        { year: { $gt: 2010 } },

        // เงื่อนไขที่ 2
        // เลือกภาพยนตร์ที่มีคะแนน IMDb ตั้งแต่ 8.0 ขึ้นไป
        { "imdb.rating": { $gte: 8.0 } }

      ]
    }
  },

  // Stage 2 : $project
  // เลือก field ที่ต้องการแสดงผล
  {
    $project: {
      _id: 0,         // ไม่แสดง _id
      title: 1,       // แสดงชื่อภาพยนตร์
      year: 1,        // แสดงปีที่ออกฉาย
      "imdb.rating": 1 // แสดงคะแนน IMDb
    }
  }

]);