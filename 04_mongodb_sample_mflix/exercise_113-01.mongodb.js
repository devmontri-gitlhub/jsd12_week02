// เลือกใช้งานฐานข้อมูล sample_mflix
use("sample_mflix");

// ใช้ Aggregation Pipeline เพื่อวิเคราะห์ข้อมูลภาพยนตร์
db.movies.aggregate([

  // Stage 1 : $match
  // กรองข้อมูลภาพยนตร์ที่ออกฉายในช่วงปี 2000 ถึง 2010
  {
    $match: {
      year: {
        $gte: 2000, // มากกว่าหรือเท่ากับ 2000
        $lte: 2010  // น้อยกว่าหรือเท่ากับ 2010
      }
    }
  },

  // Stage 2 : $group
  // จัดกลุ่มข้อมูลตามปี (year)
  // และนับจำนวนภาพยนตร์ในแต่ละปี
  {
    $group: {
      _id: "$year",        // ใช้ year เป็นตัวจัดกลุ่ม
      totalMovies: {
        $sum: 1            // นับจำนวน document ในแต่ละกลุ่ม
      }
    }
  },

  // Stage 3 : $sort
  // เรียงลำดับผลลัพธ์ตามปีจากน้อยไปมาก
  {
    $sort: {
      _id: 1
    }
  }

]);